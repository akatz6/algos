const validSudoku = (arr) => {
  const set = new Set();
  for (let i = 0; i < arr.length; i++) {
    for (let j = 0; j < arr[i].length; j++) {
      const element = arr[i][j];
      if (element !== ".") {
        const column = `${element} in column ${i}`;
        if (set.has(column)) {
          return false;
        } else {
          set.add(column);
        }
        const row = `${element} in row ${j}`;
        if (set.has(row)) {
          return false;
        } else {
          set.add(row);
        }
        const section = `${element} in section ${Math.floor(i / 3)} and ${Math.floor(j / 3)} `;
        if (set.has(section)) {
          return false;
        } else {
          set.add(section);
        }
      }
    }
  }
  return true;
};
